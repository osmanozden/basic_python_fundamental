#Karşılaştırma Soru sorma ifadeleri


x==y #demek x y ye eşitmidir demektir. 
x!=y #demek x y ye eşit değilmir demektir. 
x<y #demek x y ye küçükmüdür demektir. 
x>y #demek x y ye büyükmüdür demektir. 
x<=y #demek x y ye küçük veya eşitmidir demektir. 
x>=y #demek x y ye büyük veya eşitmidir demektir. 
